package uniquindio.fundamentos.comunidad.mundo;

public class Propietario {
	private String nombre;
	private int id;

	public Propietario(int id, String nombre) {
		
		this.id = id;
		this.nombre = nombre;
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
	
	
	

}
